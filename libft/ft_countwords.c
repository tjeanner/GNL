#include "libft.h"

int		ft_countwords(char *s, char c)
{
	int		i;
	int		nb;

	i = 0;
	nb = 0;
	while (s[i])
	{
		while (s[i] != c && s[i])
			i++;
		while (s[i] == c)
		{
			i++;
			nb = (i == 1) ? nb - 1 : nb;
		}
		nb++;
	}
	return (nb);
}
